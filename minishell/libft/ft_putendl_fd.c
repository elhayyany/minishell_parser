#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	n;

	if (!s)
		return ;
	n = '\n';
	while (*s != '\0')
	{
		write(fd, s, 1);
		s++;
	}
	write(fd, &n, 1);
}
